

a = int(input('Enter a number : '))
b = input('Enter the sign : ')
c = int(input('Enter another number number : '))


if b == "+":
    print(a+c)
elif b == "-":
    print(a - c)
elif b == "*":
    print(a * c)
elif b == "/":
    print(a / c)
else:
    print('Please Choose the correct Operator')


